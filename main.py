# 💡 Vercel을 안 쓰니까 복잡한 CORS 보안 설정은 싹 지워도 됩니다!

class ChatRequest(BaseModel):
    message: str

# 1. 정문: 사용자가 사이트에 들어오면 index.html 화면을 띄워준다.
